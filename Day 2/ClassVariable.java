public class ClassVariable {
   
    int x,y,z;
    public static void main(String[] args) {
       
        ClassVariable obj=new ClassVariable();
        
        obj.x=10;
        obj.y=20;
        obj.z=30;
        
        System.out.println("Numbers are : "+obj.x+" "+obj.y+" "+obj.z);
        
    }
}
